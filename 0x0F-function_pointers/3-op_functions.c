/**
 * op_add - Return the sum of two numbers.
 * @a: The first number
 * @b: The second number
 *
 * Return: The sum of a and b
 */

int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - Return the difference of two numbers.
 * @a: The first number
 * @b: The second number
 *
 * Return: The difference of a and b
 */

int op_sub(int a, int b)
{
        return (a - b);
}

/**
 * op_mul - Return the product of two numbers.
 * @a: The first number
 * @b: The second number
 *
 * Return: The product of a and b
 */

int op_mul(int a, int b)
{
        return (a * b);
}

/**
 * op_div - Return the devision of a number.
 * @a: The first number
 * @b: The second number
 *
 * Return: a divided by b
 */

int op_div(int a, int b)
{
        return (a / b);
}

/**
 * op_mod - Return the remainder of a number devided by another.
 * @a: The first number
 * @b: The second number
 *
 * Return: The remainder of a divided by b
 */

int op_mod(int a, int b)
{
        return (a % b);
}
